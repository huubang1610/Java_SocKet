package TestTN_TCP;

public class File_Document {
    private String DeBai;
    private String DaA;
    private String DaB;
    private String DaC;
    private String DaD;
    private String Key;

    public String getDeBai() {
        return DeBai;
    }

    public void setDeBai(String deBai) {
        DeBai = deBai;
    }

    public String getDaA() {
        return DaA;
    }

    public void setDaA(String daA) {
        DaA = daA;
    }

    public String getDaB() {
        return DaB;
    }

    public void setDaB(String daB) {
        DaB = daB;
    }

    public String getDaC() {
        return DaC;
    }

    public void setDaC(String daC) {
        DaC = daC;
    }

    public String getDaD() {
        return DaD;
    }

    public void setDaD(String daD) {
        DaD = daD;
    }

    public String getKey() {
        return Key;
    }

    public void setKey(String key) {
        Key = key;
    }

    @Override
    public String toString() {
        return "File_Document{" +
                "DeBai='" + DeBai + '\'' +
                ", DaA='" + DaA + '\'' +
                ", DaB='" + DaB + '\'' +
                ", DaC='" + DaC + '\'' +
                ", DaD='" + DaD + '\'' +
                ", Key='" + Key + '\'' +
                '}';
    }
}
